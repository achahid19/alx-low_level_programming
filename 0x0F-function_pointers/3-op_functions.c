/**
 * op_add - sum of a and b
 * @a: first integer
 * @b: second integer
 * Return: sum of two integers
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substraction of a and b
 * @a: first integer
 * @b: second integer
 * Return: sub of two integers
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication of a and b
 * @a: first integer
 * @b: second integer
 * Return: mul of two integers
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of a and b
 * @a: first integer
 * @b: second integer
 * Return: division of two integers
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulos of a and b
 * @a: first integer
 * @b: second integer
 * Return: a modulos b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
